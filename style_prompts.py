"""写作风格说明：注入 Prompt，约束标题与正文的整体调性。"""

STYLE_OPTIONS = ["干货", "幽默", "种草"]

STYLE_INSTRUCTIONS = {
    "干货": """【干货向】
- 正文用清晰分点/步骤，信息密度高，适合收藏
- 标题突出「攻略、清单、避坑、保姆级、建议收藏」
- 少用夸张梗，多用具体场景、时间、地点、数字
- 语气真诚、像过来人分享经验""",
    "幽默": """【幽默向】
- 口语化、有梗、可适当自嘲或反差，但别低俗
- 标题可有悬念、夸张修辞和 emoji，抓眼球
- 正文节奏轻快，短句多，像在跟朋友聊天
- 仍要保证信息有用，不是纯段子""",
    "种草": """【种草向】
- 第一人称体验感强：我去了、我觉得、强烈推荐
- 突出氛围感、情绪价值、拍照/打卡点、适合谁去
- 标题偏「绝绝子、宝藏、治愈、氛围感、必冲」
- 正文可有「优缺点」或「小tips」，像真心安利""",
}


def get_style_instructions(style: str) -> str:
    return STYLE_INSTRUCTIONS.get(style, STYLE_INSTRUCTIONS["干货"])
